def getStrCode(s):
    n = ord(s)
    name = getKey(n)
    idx = globals()[name][0]
    result = globals()[name][n-idx]
    return result

def prnCode(code):
    for n in code:
        print(mkCodeStr(n))

def getKey(n):
    for key in han_dict:
        if n < key:
            result = han_dict[key]
            break
    return result

def mkCodeStr(code):
    result = []
    for n in code:
        buf = '{:06b}'.format(n)
        result.append(buf)
    return result
